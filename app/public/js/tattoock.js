    //ROSTO
    function EnableDisabletxtrosto(ckrosto) {
        var txtrosto = document.getElementById("txtrosto");
        txtrosto.disabled = txtrosto.checked ? false : true;
        if (!txtrosto.disabled) {
            txtrosto.focus();
        }else{
          txtrosto.value = '';
        }
    }

